package interview;

public class ArrayCheck {

	public static void main(String[] args) {
		
		int a[]= {1,2,3,4,5};
		boolean b=false;
		for(int i=0;i<a.length;i++) {
			
			if(a[i]==5) {
				System.out.println("Value is present at:"+i);
		b=true;
		break;
			}
			if(b=false) {
				
				System.out.println("Value is not there");
			}

	}
	}

}
